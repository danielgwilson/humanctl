// Stage 5 (#71) item 8: rounded-2 (r8) is concentric with the r12 panel
// below it, 4px inset (P6). `pl-6` (24px) replaces the old `pl-8` (32px,
// not one of the eight permitted spacing steps) for the inset-indent case,
// same fix as dropdown-menu.tsx throughout this file.
function ContextMenuSubTrigger({
  className,
  inset,
  children,
  ...props
}: React.ComponentProps<typeof ContextMenuPrimitive.SubTrigger> & {
  inset?: boolean
}) {
  return (
    <ContextMenuPrimitive.SubTrigger
      data-slot="context-menu-sub-trigger"
      data-inset={inset}
      className={cn(
        "flex cursor-default items-center rounded-2 px-2 py-1.5 font-mono text-row outline-hidden select-none focus:wash-hover data-[inset]:pl-6 data-[state=open]:bg-hover [&_svg]:pointer-events-none [&_svg]:shrink-0 [&_svg:not([class*='text-'])]:text-ink-3",
        className
      )}
      {...props}
    >
      {children}
      <Icon icon={ChevronRightIcon} className="ml-auto" />
    </ContextMenuPrimitive.SubTrigger>
  )
}

